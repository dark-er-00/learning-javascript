// Creating Object with three fields
let ticket = {
    from: "Bulacan",
    to: "Nueva Ecija",
    price: 2000
};

console.log(`From : ${ticket.from}`);
console.log(`To : ${ticket.to}`);
console.log(`Price : ${ticket.price}`);

